let ans=document.getElementById("sub");
URL="https://latest.currency-api.pages.dev/v1/currencies/eur.json";
ans.addEventListener("click",async (event)=>{ 
  event.preventDefault(); 
  let amount=document.querySelector(".amount input");
  let val=amount.value;
  if(val==="" || val<0){
    amount.value=1;
    val=1;
  }
  to=to.toLowerCase();
  from=from.toLowerCase();
  let res=await fetch(URL);
  let data=await res.json();
  from_val=data.eur[from];
  to_val=data.eur[to];
  to=to.toUpperCase();
  from=from.toUpperCase();
  let outputval=val*to_val/from_val;
  let output= val.toString() + ' ' +from+ ' is equal to '+ outputval.toString() +' '+ to;
  let msg=document.querySelector(".msg");
  msg.innerText=output;
})
